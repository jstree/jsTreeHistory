    /**
     * 로그 유틸 : 비밀번호 *로 표시 함수
     */
    public static String getToPasswordString(String pass) {
        
        if (pass == null) {
            return "";
        }
        
        String output = "";
        for (int i = 0; i < pass.length(); i++) {
            output += "*";
        }
        
        return output;
    }
