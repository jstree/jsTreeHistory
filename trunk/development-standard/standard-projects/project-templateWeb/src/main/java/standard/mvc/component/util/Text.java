    /**
     * 문자열의 각 문자가 차지하는 픽셀을 세어 문자열이 표현될 때 너비가 정해진 픽셀을 넘지 않도록 문자열을 잘라준다.
     * 
     * @param src
     *            원본 문자열
     * @param width
     *            픽셀 너비
     * @param postfix
     *            문자열을 자를 때 생략 기호로 붙일 문자열
     * @param font
     *            폰트 이름 (gulim, batang, dotum, gungsuh, arial, times, tahoma, verdana만 된다)
     * @param pixel
     *            폰트 크기 (pixel 단위, 8pt = 11px, 9pt = 12px, 10pt = 13px, 11pt = 15px, 12pt = 16px, 13pt = 17px, 14pt = 19px이다)
     * @return 잘려진 문자열
     */
    public static String limitStringByFontWidth(String src, int width, String postfix, String font, int point) {
        if (null == src || 0 == src.length()) {
            return src;
        }
        int[][] fontMap = findFontMap(font);
        
        int size = 0;
        int len = 0;
        int pixel = pointToPixel(point);
        if (!StringUtils.isEmpty(postfix)) {
            for (int i = 0; i < len; i++) {
                size += getFontWidth(postfix.charAt(i), fontMap, pixel);
            }
        }
        int idx = 0;
        len = src.length();
        while (idx < len) {
            size += getFontWidth(src.charAt(idx), fontMap, pixel);
            if (size >= width) {
                break;
            }
            idx++;
        }
        
        if (idx < len) {
            String result = src.substring(0, idx);
            if (!StringUtils.isEmpty(postfix)) {
                result += postfix;
            }
            return result;
        } else {
            return src;
        }
    }
    
    public static int getTextWidth(String src, String font, int point) {
        if (null == src || 0 == src.length()) {
            return 0;
        }
        int[][] fontMap = findFontMap(font);
        
        int size = 0;
        int len = src.length();
        int pixel = pointToPixel(point);
        for (int i = 0; i < len; i++) {
            size += getFontWidth(src.charAt(i), fontMap, pixel);
        }
        return size;
    }
    
    private static int getFontWidth(char c, int[][] font, int pixel) {
        if (c > 127) {
            return pixel;
        }
        if (c >= 32 && c <= 127) {
            return font[pixel - 11][c - 32];
        }
        return (0 == pixel % 2 ? pixel / 2 : pixel / 2 + 1);
    }
    
